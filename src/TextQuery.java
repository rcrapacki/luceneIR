
public class TextQuery {
	private String num;
	private String ESTitle;
	private String ESDesc;
	private String ESNarr;
	
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getESTitle() {
		return ESTitle;
	}
	public void setESTitle(String eSTitle) {
		ESTitle = eSTitle;
	}
	public String getESDesc() {
		return ESDesc;
	}
	public void setESDesc(String eSDesc) {
		ESDesc = eSDesc;
	}
	public String getESNarr() {
		return ESNarr;
	}
	public void setESNarr(String eSNarr) {
		ESNarr = eSNarr;
	}
}
